    private void jbModificarLocacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbModificarLocacionActionPerformed
        // TODO add your handling code here:
        String nombreLocacion, domicilioLocacion;
        boolean estadoLocacion;
        nombreLocacion = jtNombreLocacion.getText();
        domicilioLocacion = jtDomicilioLocacion.getText();
        estadoLocacion = "Disponible".equals(jcbLocacion.getSelectedItem().toString());
        Locacion locacion = new Locacion(nombreLocacion, domicilioLocacion, estadoLocacion);
        locacion.setId(id_agenda);
        
        try {
            LocacionControlador locacionControlador = new LocacionControlador();
            locacionControlador.modificar(locacion);
            javax.swing.JOptionPane.showMessageDialog(this, "Datos modificados correctamente.");
            
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, e);
        }
        
    }//GEN-LAST:event_jbModificarLocacionActionPerformed

